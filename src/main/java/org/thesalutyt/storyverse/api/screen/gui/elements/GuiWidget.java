package org.thesalutyt.storyverse.api.screen.gui.elements;

public class GuiWidget {
    protected Double x;
    protected Double y;
    protected Double width;
    protected Double height;

    public GuiWidget(Double x, Double y, Double width, Double height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
